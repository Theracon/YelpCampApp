function seedDB() {
    // Remove all campgrounds
    Campground.remove({}, function(err) {
        if (err) {
            console.error(err);
        } else {
            console.log('REMOVED CAMPGROUNDS!');
            // Add a few campgrounds
            data.forEach(function(eachData) {
                Campground.create(eachData, function(err, campground) {
                    if (err) {
                        console.log('error from campground creation');
                    } else {
                        console.log('Added a new campground');
                        // Add a comment for each campground
                        Comment.create({
                                text: 'This is just a random comment',
                                author: 'Random_Guy',
                            },
                            function(err, comment) {
                                if (err) {
                                    console.log('error from comment creation');
                                } else {
                                    campground.comments.push(comment);
                                    campground.save();
                                    console.log('One comment created');
                                }
                            },
                        );
                    }
                });
            });
        }
    });
}

